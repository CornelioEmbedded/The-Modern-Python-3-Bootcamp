## a < b
## a > b
## a <= b
## a >= b

num = input('Give me a number: ')
num1 = input('Give me another number: ')

if num == num1:
    print(f'{num} is equal to {num1}')
elif num < num1:
    print(f'{num} is minor than {num1}')
elif num > num1:
    print(f'{num} is greater than {num1}')